    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        try {
            String id=JOptionPane.showInputDialog(null,"Ingresar el id del repuesto a modificar");
            Object[] datos=new Object[Proyecto2PS19.cantidadrepuestos];
            String aux;int corridas=0;
            for (int i = 0; i < Proyecto2PS19.cantidadrepuestos; i++) {
                corridas++;
                datos[i]=Proyecto2PS19.pilarepuestos.pop();
                aux=(String) datos[i];
                String[] repuesto=aux.split("-");
                if(id.equals(repuesto[0])){
                    String cadena,nnombre,nmarca,nmodelo,nexistencias,nprecio;
                    
                    nnombre=JOptionPane.showInputDialog("Ingrese el nombre modificado, el nombre actual es: "+repuesto[1]);
                    nmarca=JOptionPane.showInputDialog("Ingrese la marca a modificar, la marca actual es: "+repuesto[2]);
                    nmodelo=JOptionPane.showInputDialog("Ingrese el modelo modificado, el modelo actual es: "+repuesto[3]);
                    nexistencias=JOptionPane.showInputDialog("Ingrese la cantidad nueva de existencias, en existencia hay: "+repuesto[4]);
                    nprecio=JOptionPane.showInputDialog("Ingrese el nuevo precio, el precio actual es: "+repuesto[5]);
                    
                    cadena=id+"-"+nnombre+"-"+nmarca+"-"+nmodelo+"-"+nexistencias+"-"+nprecio;
                    datos[i]=cadena;
                    JOptionPane.showMessageDialog(null, "Dato modificado");
                }   
            }
        for (int i = Proyecto2PS19.cantidadrepuestos-1; i >=0 ; i--) {
            Proyecto2PS19.pilarepuestos.push(datos[i]);
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

