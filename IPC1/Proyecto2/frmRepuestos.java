    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        mod.setNumRows(0);
        Object[] datos=new Object[Proyecto2PS19.cantidadrepuestos];
        String aux="";
        for (int i = 0; i < Proyecto2PS19.cantidadrepuestos; i++) {
            datos[i]=Proyecto2PS19.pilarepuestos.pop();
            aux=(String) datos[i];
            String[] repuesto=aux.split("-");
            mod.addRow(repuesto);
        }
        for (int i = Proyecto2PS19.cantidadrepuestos-1; i >=0 ; i--) {
            Proyecto2PS19.pilarepuestos.push(datos[i]);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

