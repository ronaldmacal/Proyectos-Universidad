    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            if(txtExistencias.getText().equals("") || txtIDRepuesto.getText().equals("")|| txtMarca.getText().equals("") || txtModelo.getText().equals("") || txtNombre.getText().equals("") || txtPrecio.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Hay campos vacíos, no se puede agregar el repuesto");
            }else{
                String linea;
                linea=txtIDRepuesto.getText()+"-"+txtNombre.getText()+"-"+txtMarca.getText()+"-"+txtModelo.getText()+"-"+txtExistencias.getText()+"-"+txtPrecio.getText();
                Proyecto2PS19.cantidadrepuestos++;
                Proyecto2PS19.pilarepuestos.push(linea);
                JOptionPane.showMessageDialog(null, "Registro guardado con éxito");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            String id=JOptionPane.showInputDialog(null,"Ingresar el id del repuesto a eliminar");
            int respuesta=JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar el repuesto?");
            int posicion=-1;
            if(respuesta==0){
                Object[] datos=new Object[Proyecto2PS19.cantidadrepuestos];
                String aux="";
                for (int i = 0; i < Proyecto2PS19.cantidadrepuestos; i++) {
                    datos[i]=Proyecto2PS19.pilarepuestos.pop();
                    aux=(String) datos[i];
                    String[] repuesto=aux.split("-");
                    if(repuesto[0].equals(id)){
                        posicion=i;
                    }
                }
                if(posicion==-1){
                    JOptionPane.showMessageDialog(null, "Ingrese un id válido");
                    for (int i = Proyecto2PS19.cantidadrepuestos-1; i >=0 ; i--) {
                        Proyecto2PS19.pilarepuestos.push(datos[i]);
                    }
                }else{
                    for (int i = Proyecto2PS19.cantidadrepuestos-1; i >=0 ; i--) {
                        if(datos[i]==datos[posicion]){
                            Proyecto2PS19.cantidadrepuestos--;
                        }else{
                            Proyecto2PS19.pilarepuestos.push(datos[i]);
                        }
                    }
                    JOptionPane.showMessageDialog(null, "Repuesto eliminado con éxito");
                }
                
            }
        } catch (Exception e) {
        }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

